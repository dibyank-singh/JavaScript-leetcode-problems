//  Series of JavaScript Problems based on Object

// 1. How do you add a property to an object?

const myObj = {name: 'John', age: 30};

// Dot notation
myObj.city = 'New York';

// Bracket notation
myObj['country'] = 'USA';

console.log(myObj); // Output: {name: 'John', age: 30, city: 'New York', country: 'USA'}


